import mongoose from "mongoose";

const sweetModel = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    category: {
        type: String,
        required: true
    },
    quantity: {
        type: Number,
        required: true
    },
    price: {
        type: Number,
        required: true
    }
}, { timestamps: true });

export const Sweet = mongoose.model("Sweet", sweetModel);